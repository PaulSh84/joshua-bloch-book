package lambdasandstreams.favoruseofstandardfunctionalinterfaces;

public class SixBasicFunctionalInterfaces {

    /**
     * The Operator interfaces represent functions whose result and argument are the same
     */

    /**
     * The Predicate interface represents a function that takes argument and returns a boolean.
     */

    /**
     * The Function interface represents a function whose argument and return types differ.
     */

    /**
     * The Supplier interface represents a function that takes no arguments and returns ("supplies") a value.
     */

    /**
     * The Consumer interface represents a function that takes and argument and returns nothing, consuming its argument.
     */


}

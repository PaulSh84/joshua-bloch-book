package classesandinterfaces.minimizemutability;

public class HowToMakeClassImmutable {

    /**
     * 1. Don't provide methods that modify object's state (no setters)
     */

    /**
     * 2. Ensure class cannot be extended (final class)
     */

    /**
     *
     * 3. Make all fields FINAL
     */

    /**
     *
     * 4.Make all fields PRIVATE
     */

    /**
     *
     * 5. Ensure exclusive access to any mutable component.
     */
}
